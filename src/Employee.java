public class Employee {
    String full_name;
    String position;
    String email;
    String phone_number;
    int salary;
    int age;

    public Employee (String full_name, String position, String email,
                     String phone_number, int salary, int age) {
        this.full_name = full_name;
        this.position = position;
        this.email = email;
        this.phone_number = phone_number;
        this.salary = salary;
        this.age = age;
    }



}
